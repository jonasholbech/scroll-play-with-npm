export function buildDOM(imageSets, scrollContainer) {
  imageSets.forEach((set, index) => {
    const el = document.createElement("div");
    el.classList.add("inner-grid");
    el.dataset.id = index;
    set.forEach((imgSrc) => {
      const img = document.createElement("img");
      img.src = imgSrc;
      el.appendChild(img);
    });
    scrollContainer.appendChild(el);
  });
}
